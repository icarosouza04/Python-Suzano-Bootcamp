# Atribuição de valores

saldo = 500 # Atribuição simples
print(saldo)

saldo += 200 # Atribuição com adição (Mantém o valor outrora existente e adiciona o valor informado ao mesmo.)
print(saldo)

saldo -= 100 # Atribuição com subtração (Mesma lógica do anterior, mudando os operadores.)
print(saldo)

saldo *= 10 # Atribuição com multiplicação
print(saldo)

saldo /= 5 # Atribuição com divisão
print(saldo)

saldo //= 5 # Atribuição com divisão inteira
print(saldo)

saldo %= 480 # Atribuição com módulo
print(saldo)

saldo **= 2 # Atribuição com exponenciação
print(saldo)
