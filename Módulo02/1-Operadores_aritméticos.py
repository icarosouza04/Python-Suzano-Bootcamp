# Operadores aritméticos

print(1 + 1) # Adição
print(10 - 2) # Subtração
print(4 * 3) # Multiplicação
print(12 / 3) # Divisão
print(12 // 2) # Divisão inteira
print(10 % 3) # Módulo
print(2 ** 3) # Exponenciação

# Ordem de precedência: (), **, */, +- Da esquerda para a direita.

print(10 - 5 * 2)
print((10 - 5) * 2)
print(10 ** 2 * 2)
print(10 ** (2 * 2))
print(10 / 2 * 4)

